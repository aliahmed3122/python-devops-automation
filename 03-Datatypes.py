# --------------------------------------------
# List / Collection of multi datatypes, enclosed in square brackets.

first_list = [str1 , "DevOps" , num1 , 45 , flt1 , 1.5]

# Printing a List
print(first_list)
print("Variable first_list is ",type(first_list))

# --------------------------------------------
# Tuple / Collection of multi datatypes, enclosed in round bracket.

first_tuple = (str1 , "DevOps" , num1 , 45 , flt1 , 1.5)

# Printing a Tuple
print(first_tuple)
print("Variable first_tuple is ",type(first_tuple))


# --------------------------------------------
# Dictionary / Elements in the dictionary are always in pairs (Key:Value), curly brackets.

first_dictionary = {"name":"Ali", "Age":22 , "Weight":65 , "Exercises":["Reading", "Writing"]}

